
public class Data {
	private String first;
	private String last;
	private int id;
	
	public String getFirst() {
		return first;
	}
	
	public void setFirst(String firstname) {
		first = firstname;
	}
	
	public String getLast() {
		return last;
	}
	
	public void setLast(String lastname) {
		last = lastname;
	}
	
	public int getID() {
		return id;
	}
	
	public void setID(int x) {
		id = x;
	}

}
