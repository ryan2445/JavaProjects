import java.util.*;
public class Learning {
	
	private String name;
	private int age;
	private String color;
	private String breed;
	
	public Learning(){
		name="null";
		age=0;
		color="null";
		breed="null";
	}
	public Learning(String name, int age, String color, String breed) {
		this.name=name;
		this.age=age;
		this.color=color;
		this.breed=breed;
	}

}
